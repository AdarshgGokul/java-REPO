package FullStack;

public class StringtoInteger {
	public static void main(String [] args) {
		
		
		//String to Integer
		
		String str= "100";
		int i=Integer.parseInt(str);
		System.out.println(i);
		
		// Integer to String
		
		int tr=200;
		String a=Integer.toString(tr);
		System.out.println(a);
	}

}
