package FullStack;

public class Encapsulation {
	
private	String food;
private	int price;
	
public String getfood() {
	return food;
}

public void setfood(String food) {
	this.food =food;
}

public int getprice() {
	return price;
}
public void setprice(int price) {
	this.price = price;
}

}
